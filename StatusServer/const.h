// Defer类  利用RAII机制，在函数内调用构造函数，函数执行完自动调用Defer析构函数，调用lambda表达式
class Defer {
public:
	// 接受一个lambda表达式或者函数指针
	Defer(std::function<void()> func) : func_(func) {}

	// 析构函数中执行传入的函数
	~Defer() {
		func_();
	}

private:
	std::function<void()> func_;
};

